let age = 50;

switch (age) {
    case 10:
        alert('You are a kid...');
        console.log('case 10 age');
        break;
    case 50:
        alert('You are young...');
        console.log('case 50 age');
        break;
    case 100:
        alert('You are great...');
        console.log('case 100 age');
        break;

    default:
        console.log('Wrong...');
        break;
}